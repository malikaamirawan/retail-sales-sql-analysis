-- Level 1 -> Bsic Analysis

-- 1-Show all customers who live in Karachi.
-- 2-Display all products in the Electronics category.
-- 3-Show all orders sorted by newest order date first.
-- 4-Find the total number of orders.
-- 5-Find the average price of products



-- 1-Show all customers who live in Karachi.

SELECT *
FROM Customer
WHERE city = 'Karachi';


-- 2-Display all products in the Electronics category.

SELECT *
FROM products
WHERE category = 'Electronics';

-- 3-Show all orders sorted by newest order date first.

SELECT *
FROM orders
ORDER BY order_date DESC;

-- 4-Find the total number of orders.

SELECT COUNT(*) AS Total_Number_of_Orders
FROM Orders;

-- 5-Find the average price of products

SELECT AVG(price) AS Average_Price_product
FROM products;


-- Level 2 — Product Insights

-- 6-Find the most expensive product.

-- 7-Find the cheapest product.

-- 8-Show total number of products in each category.

-- 9-Find the total quantity of items sold.

-- 10-Show products and their prices sorted from highest to lowest.


-- 6-Find the most expensive product.

SELECT TOP 1*
FROM products
ORDER BY price DESC;

-- 7-Find the cheapest product.
SELECT TOP 1*
FROM products
ORDER BY price ASC;

-- 8-Show total number of products in each category.

SELECT 
	COUNT(*) AS Total_Number_of_products_category,
	category
FROM Products
GROUP BY category;


-- 9-Find the total quantity of items sold.

SELECT SUM(qunatity) AS total_items_Sold
FROM Orders;

-- 10-Show products and their prices sorted from highest to lowest.

SELECT *
FROM Products
ORDER BY price DESC;

-- Level 3 — JOIN 

-- 11-Show customer name and their order dates.

-- 12-Show product name and quantity ordered.

-- 13️-Show customer name, product name, and quantity purchased.

-- 14-Show customer name and product they purchased.

-- 15-Show all orders with customer city.


